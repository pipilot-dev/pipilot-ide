/**
 * Bundle PiPilot server code for Tauri sidecar distribution.
 *
 * Produces:
 *   src-tauri/resources/server-bundle.mjs  (agent server)
 *   src-tauri/resources/cloud-bundle.mjs   (cloud server)
 *
 * Native modules (node-pty, puppeteer-core) are marked external —
 * they ship as-is in src-tauri/resources/node_modules/.
 */

import { build } from "esbuild";
import { cpSync, existsSync, mkdirSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const RESOURCES = resolve(ROOT, "src-tauri", "resources");

// Ensure output dir exists
mkdirSync(RESOURCES, { recursive: true });

// Native modules that cannot be bundled — they contain .node C++ addons
const EXTERNALS = [
  "node-pty",
  "puppeteer-core",
  // Node built-ins are handled by platform: "node"
];

/** Shared esbuild options */
const shared = {
  bundle: true,
  platform: "node",
  target: "node20",
  format: "esm",
  sourcemap: false,
  minify: false, // keep readable for debugging
  external: EXTERNALS,
  banner: {
    js: [
      "// PiPilot IDE server bundle — generated by scripts/bundle-server.mjs",
      "import { createRequire } from 'module';",
      "const require = createRequire(import.meta.url);",
    ].join("\n"),
  },
};

console.log("[bundle] Bundling server/index.ts → server-bundle.mjs ...");
await build({
  ...shared,
  entryPoints: [resolve(ROOT, "server", "index.ts")],
  outfile: resolve(RESOURCES, "server-bundle.mjs"),
});

console.log("[bundle] Bundling server/cloud.ts → cloud-bundle.mjs ...");
await build({
  ...shared,
  entryPoints: [resolve(ROOT, "server", "cloud.ts")],
  outfile: resolve(RESOURCES, "cloud-bundle.mjs"),
});

// Copy native module packages so require() can find them at runtime
console.log("[bundle] Copying native modules to resources/node_modules/ ...");

const nativeModules = ["node-pty"];
for (const mod of nativeModules) {
  const src = resolve(ROOT, "node_modules", mod);
  const dest = resolve(RESOURCES, "node_modules", mod);
  if (existsSync(src)) {
    mkdirSync(dirname(dest), { recursive: true });
    cpSync(src, dest, { recursive: true, dereference: true });
    console.log(`  ✓ ${mod}`);
  } else {
    console.warn(`  ⚠ ${mod} not found in node_modules — skipping`);
  }
}

// Also copy puppeteer-core (it's pure JS but has complex internal imports)
const puppeteerSrc = resolve(ROOT, "node_modules", "puppeteer-core");
const puppeteerDest = resolve(RESOURCES, "node_modules", "puppeteer-core");
if (existsSync(puppeteerSrc)) {
  cpSync(puppeteerSrc, puppeteerDest, { recursive: true, dereference: true });
  console.log("  ✓ puppeteer-core");
  // Also copy chromium-bidi and devtools-protocol if present (puppeteer peer deps)
  for (const peer of ["chromium-bidi", "devtools-protocol", "debug", "ms"]) {
    const peerSrc = resolve(ROOT, "node_modules", peer);
    const peerDest = resolve(RESOURCES, "node_modules", peer);
    if (existsSync(peerSrc)) {
      cpSync(peerSrc, peerDest, { recursive: true, dereference: true });
      console.log(`  ✓ ${peer} (puppeteer peer)`);
    }
  }
} else {
  console.warn("  ⚠ puppeteer-core not found — screenshot feature won't work");
}

console.log("[bundle] Done! Bundles written to src-tauri/resources/");
