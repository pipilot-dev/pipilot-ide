/**
 * Bundle PiPilot server code for Tauri sidecar distribution.
 *
 * Produces:
 *   src-tauri/resources/server-bundle.cjs  (agent server)
 *   src-tauri/resources/cloud-bundle.cjs   (cloud server)
 *
 * Native modules (node-pty, puppeteer-core) are marked external —
 * they ship as-is in src-tauri/resources/node_modules/.
 */

import { build } from "esbuild";
import { cpSync, existsSync, mkdirSync, rmSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const RESOURCES = resolve(ROOT, "src-tauri", "resources");

// Ensure output dir exists
mkdirSync(RESOURCES, { recursive: true });

// Native modules that cannot be bundled — they contain .node C++ addons
const EXTERNALS = [
  "node-pty",
  "puppeteer-core",
  // Node built-ins are handled by platform: "node"
];

/** Shared esbuild options */
const shared = {
  bundle: true,
  platform: "node",
  target: "node20",
  format: "cjs",
  sourcemap: false,
  minify: false, // keep readable for debugging
  external: EXTERNALS,
  banner: {
    js: "// PiPilot IDE server bundle — generated by scripts/bundle-server.mjs",
  },
};

console.log("[bundle] Bundling server/index.ts → server-bundle.cjs ...");
await build({
  ...shared,
  entryPoints: [resolve(ROOT, "server", "index.ts")],
  outfile: resolve(RESOURCES, "server-bundle.cjs"),
});

console.log("[bundle] Bundling server/cloud.ts → cloud-bundle.cjs ...");
await build({
  ...shared,
  entryPoints: [resolve(ROOT, "server", "cloud.ts")],
  outfile: resolve(RESOURCES, "cloud-bundle.cjs"),
});

// Copy native module packages so require() can find them at runtime
console.log("[bundle] Copying native modules to resources/node_modules/ ...");

const nativeModules = ["node-pty"];
for (const mod of nativeModules) {
  const src = resolve(ROOT, "node_modules", mod);
  const dest = resolve(RESOURCES, "node_modules", mod);
  if (existsSync(src)) {
    if (existsSync(dest)) rmSync(dest, { recursive: true });
    mkdirSync(dirname(dest), { recursive: true });
    cpSync(src, dest, { recursive: true, dereference: true });
    console.log(`  ✓ ${mod}`);
  } else {
    console.warn(`  ⚠ ${mod} not found in node_modules — skipping`);
  }
}

// puppeteer-core is NOT shipped — screenshot.ts lazy-loads it with try/catch
// and fails gracefully if unavailable. This avoids bundling its many peer deps
// (@puppeteer/browsers, chromium-bidi, devtools-protocol, etc.)
console.log("  ℹ puppeteer-core skipped (lazy-loaded, fails gracefully)");

console.log("[bundle] Done! Bundles written to src-tauri/resources/");
